            <h2 style="text-align: center"><a href="#">Bergabung Bersama Kami Membangun Bangsa </a></h2>
            <p>
                "PDAM Bandung membuka kesempatan bagi para pemuda-pemudi Indonesia terbaik untuk
                menjadi Calon Pegawai Negeri Sipil PDAM Bandung, mengabdi dan berkarya untuk
                Indonesia. </p>
            <p mce_style="text-align: justify;"><strong>VISI, MISI dan MOTTO PDAM TIRTAWENING KOTA
                    BANDUNG</strong></p>
            <p mce_style="text-align: justify;"><strong>VISI :</strong></p>
            <p mce_style="text-align: justify;">Terpenuhinya kebutuhan masyarakat akan pelayanan air
                minum dan air limbah yang berwawasan lingkungan, dan berkelanjutan.</p>
            <p mce_style="text-align: justify;"><strong>MISI :</strong></p>
            <ul mce_style="text-align: justify;">
                <li>Memberikan pelayanan dan kemanfaatan umum kepada seluruh masyarakat melalui
                    pelayanan air minum dan air limbah yang berwawasan lingkungan.
                </li>
                <li>Mewujudkan pengelolaan keuangan perusahaan secara mandiri melalui pendapatan
                    yang diperoleh dari masyarakat dan dikembalikan lagi kepada masyarakat guna
                    meningkatkan pelayanan dan penyediaan air minum maupun sarana air limbah.
                </li>
                <li>Meningkatkan pengolahan kualitas air minum dan air limbah yang sesuai dengan
                    standar kesehatan dan lingkungan.
                </li>
                <li>Mewujudkan penambahan cakupan pelayanan air minum dan air limbah yang
                    disesuaikan dengan pertambahan penduduk kota Bandung.
                </li>
            </ul>
            <p mce_style="text-align: justify;"><strong>MOTTO : " TIRTA DAYA MARTA UTAMA
                    "</strong><br>
                <br>
                Pengertian :</p>
            <p mce_style="text-align: justify;">Tirta = Air , Daya = Kekuatan, Tenaga, Yang
                menyebabkan sesuatu bergerak, Marta = Kehidupan, Utama = Utama, Baik</p>
            <p mce_style="text-align: justify;">Filosofi :<br>
            </p>
            <p mce_style="text-align: justify;">Tuhan menciptakan air untuk memenuhi kebutuhan semua
                mahluk hidup. ;Air memiliki peranan yang sangat penting karena 60% kandungan di
                dalam tubuh manusia adalah air sehingga air menjadi unsur utama dalam kehidupan
                manusia agar dapat berdaya dan teurs berkarya.<br>
            </p>
            <p mce_style="text-align: justify;"><strong>MAKSUD DAN TUJUAN PDAM KOTA BANDUNG</strong><br>
                <br>
                Sesuai Peraturan Daerah Kota Bandung Nomor 15 Tahun 2009<br>
                <br>
                PDAM Tirtawening Kota Bandung didirikan dengan maksud dan tujuan :<br>
            </p>
            <ul mce_style="text-align: justify;">
                <li>Menyelenggarakan usaha pengelolaan air minum dan air limbah bagi kepentingan
                    umum dalam jumlah dan mutu yang memadai serta usaha lainnya di bidang air minum
                    dan air limbah.
                </li>
                <li>Memupuk keuntungan dan melaksanakan penugasan Pemerintah Daerah di bidang air
                    minum dan air limbah dalam rangka menunjang pembangunan dengan menetapkan
                    prinsip perusahaan.
                </li>
            </ul>
            <p mce_style="text-align: justify;"><strong>TUGAS &amp; FUNGSI PDAM</strong><br>
                <br>
                Tugas pokok Perusahaan Daerah Air Minum Tirtawening Kota Bandung sesuai Peraturan
                Walikota Bandung Nomor 236 Tahun 2009 adalah bergerak di bidang pengelolaan air
                minum dan pengelolaan sarana air limbah di daerah, untuk meningkatkan kesejahteraan
                masyarakat yang mencakup aspek ekonomi, sosial, kesehatan dan pelayanan umum.<br>
                <br>
                Dalam melaksanakan tugas pokok sebagaimana dimaksud, PDAM menyelenggarakan
                fungsi-fungsi sebagai berikut :<br>
            </p>
            <ol mce_style="text-align: justify;">
                <li>Perumusan kebijakan dan strategi usaha pengelolaan air minum dan sarana air
                    limbah ;
                </li>
                <li>Melaksanakan pelayanan umum/jasa kepada masyarakat konsumen dalam penyediaan air
                    minum dan sarana air limbah ;
                </li>
                <li>Perencanaan pembangunan, pemeliharaan dan pengawasan sarana dan prasarana air
                    minum dan air limbah ;
                </li>
                <li>Pengelolaan keuangan Perusahaan Daerah untuk membiayai kelangsungan hidup
                    Perusahaan Daerah dan Pembangunan Daerah ;
                </li>
                <li>Pengelolaan pegawai PDAM ;</li>
                <li>Evaluasi dan pelaporan pelaksanaan program kegiatan dan usaha PDAM kepada
                    Walikota melalui Badan Pengawas.
                </li>
            </ol>
